const X_VIEW = 9
const Y_VIEW = 4

export default {
  getLocalMap: (playerX, playerY) => new Promise((resolve) => {
    const minY = Math.max(0, playerY - Y_VIEW)
    const maxY = Math.min(WORLD_MAP.length, playerY + Y_VIEW + 1)
    const localMap = WORLD_MAP.slice(minY, maxY).map((row, y) => {
      const minX = Math.max(0, playerX - X_VIEW)
      const maxX = Math.min(row.length, playerX + X_VIEW + 1)
      return row.slice(minX, maxX).map((locationType, x) => {
        return {
          x: x + minX - playerX + X_VIEW,
          y: y + minY - playerY + Y_VIEW,
          locationType
        }
      })
    })
    resolve({ localMap })
  })
}
