class Location(LocalRecord):
    RECORDS = []

    def __init__(self, **fields):
        super().__init__(**fields)

    def as_dict(self):
        return {
            'id': self.id,
            'x': self.x,
            'y': self.y,
            'location_type': self.location_type_id,
            'castle_id': self.castle_id,
        }

    @property
    def castle_id(self):
        castle = Castle.by_coords(self.x, self.y)
        if not castle:
            return None
        else:
            return castle.id


